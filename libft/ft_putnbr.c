#include "libft.h"

size_t	ft_putnbr(int n)
{
	size_t	len;

	ft_putnbr_fd(n, 1);
	if (n == -2147483648)
		return (11);
	if (n == 0)
		return (1);
	len = 0;
	if (n < 0)
	{
		len++;
		n *= -1;
	}
	while (n > 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}
