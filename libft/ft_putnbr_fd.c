#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned char	c;

	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	else
	{
		c = n + '0';
		write(fd, &c, 1);
		return ;
	}
	c = (n % 10) + '0';
	write(fd, &c, 1);
}
